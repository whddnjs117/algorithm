
n = int(input());

# dp[1] = 1²                                           / 1     
# dp[2] = 1² + 1²                                      / 2
# dp[3] = 1² + 1² + 1²                                 / 3
# dp[4] = 2² // 1² + 1² + 1² + 1²                      / 1 4
# dp[5] = 2² + 1² // 1² + 1² + 1² + 1² + 1²            / 2 5
# dp[6] = 2² + 1² + 1² // 1² * 6                       / 3 6
# dp[7] = 2² + 1² + 1² + 1² // 1² * 7                  / 4 7
# dp[8] = 2² + 2² // 2² + 1² + 1² + 1² + 1² // 1² * 8  / 2 5 8
# dp[9] = 3² // 2² + 2² + 1² // 2² + 1² + 1² + 1² + 1² + 1² // 1² *9 

# dp[1] = 1
# dp[2] = dp[1] + 1
# dp[3] = dp[2] + 1
# dp[4] = dp[3] + 1 , 1
# dp[5] = dp[4] + 1
# dp[6] = dp[5] + 1
# dp[7] = dp[6] + 1
# dp[8] = dp[7] + 1 , dp[4] + 1
# dp[9] = 1
# dp[n] = dp[n-i*i] + 1



dp = [0];

for i in range(1,n+1):
    dp.append(i);
    for j in range(1,i):
        if j*j>i:
            break;
        
        if dp[i] > dp[i-j*j] + 1:
            dp[i] = dp[i-j*j] + 1;

print(dp[n]);

